#include"stm32f10x.h"

int main(void){
	
	/***********初始化*************/
	/*
			1、开启GPIO端口时钟 
			2、端口清零
			3、配置输入/输出模式、输出速率
	*/
	//开启端口时钟
	RCC_APB2ENR |= (1<<3);
	
	//清零控制PB0的端口位
	GPIOB_CRL &= ~( 0x0f );
	//配置PB0为通用推挽输出，速度为10M
	GPIOB_CRL |= (1<<4*1);
	
	
	/*********控制引脚电平输出*********/
	//PB0输出低电平
	//GPIOB_ODR &= ~(1<<3);
	GPIOB_ODR =  0x0000000D;
	
	/*
		&=清零
		|=置位
	*/
	
	while(1);

}
